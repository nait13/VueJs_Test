<template>
  <div class="modal" v-if="show" @click="hideModal">
    <div @click.stop class="modal__content">
      <slot></slot>
    </div>
  </div>
</template>

<script>
export default {
  name:'my-modal',
  props:{
    show:{
      type:Boolean,
      default:false,
    }
  },
  methods:{
    hideModal(){
      this.$emit('update:show',false)
    }
  }
}

</script>

<style scoped>
.modal{
  top: 0;
  right: 0;
  left: 0;
  bottom: 0;
